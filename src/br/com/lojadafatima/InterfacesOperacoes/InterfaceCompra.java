    public boolean camposobrigatoriospreenchidos() {
        if (TfDescNotaCompra.getText().equals("")) {
            msg.CampoObrigatorioNaoPreenchido(LbNotificacao, "Digite o c칩digo da nota do fornecedor!");
            TfDescNotaCompra.grabFocus();
            return false;
        }
        if (TfCodFuncionario.getText().equals("") || TfFuncionario.getText().equals("")) {
            msg.CampoObrigatorioNaoPreenchido(LbNotificacao, "Insira o Funcion치rio que esta realizando o antendimento nesta compra!");
            TfCodFuncionario.grabFocus();
            return false;
        }
        if (TfCodFornecedor.getText().equals("") || TfFornecedor.getText().equals("")) {
            msg.CampoObrigatorioNaoPreenchido(LbNotificacao, "Insira qual o Fornecedor desta compra!");
            TfCodFornecedor.grabFocus();
            return false;
        }
        if (TfCodCondicaoPgto.getText().equals("") || TfCondicaoPgto.getText().equals("")) {
            msg.CampoObrigatorioNaoPreenchido(LbNotificacao, "Insira a condi칞칚o de pagamento escolhida para esta compra!");
            TfCodCondicaoPgto.grabFocus();
            return false;
        }
        if (TbCompra.getRowCount() < 1 && TbCompraServ.getRowCount() < 1) {            
            msg.CampoObrigatorioNaoPreenchido(LbNotificacao, "Insira os produtos ou servi칞os desta compra!");
            return false;
        }
        return true;
    }

