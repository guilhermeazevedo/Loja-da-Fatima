    public boolean camposobrigatoriospreenchidos() {
        if (TfCodFuncionario.getText().equals("") || TfFuncionario.getText().equals("")) {
            msg.CampoObrigatorioNaoPreenchido(LbNotificacao, "Informe qual funcion치rio realizou o atendimento nesta venda!");
            TfCodFuncionario.grabFocus();
            return false;
        }
        if (TfCodCliente.getText().equals("") || TfCliente.getText().equals("")) {
            msg.CampoObrigatorioNaoPreenchido(LbNotificacao, "Informe qual cliente est치 realizando a venda!");
            TfCodCliente.grabFocus();
            return false;
        }
        if (TfCodCondicaoPgto.getText().equals("") || TfCondicaoPgto.getText().equals("")) {
            msg.CampoObrigatorioNaoPreenchido(LbNotificacao, "Informe qual a condi칞칚o de pagamento escolhida pelo cliente!");
            TfCodCondicaoPgto.grabFocus();
            return false;
        }
        if (TbVenda.getRowCount() == 0 && TbVendaServ.getRowCount() == 0) {
            msg.CampoObrigatorioNaoPreenchido(LbNotificacao, "Insira os produtos ou servi칞os desta venda!");
            TfCodProduto.grabFocus();
            return false;
        }
        return true;
    }

