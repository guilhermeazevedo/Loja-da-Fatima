    private void BtIncluirProdutoTabelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtIncluirProdutoTabelaActionPerformed
        boolean inclui = true;
        limpar.Limpar(TbServicos);
        TfValorTotalOperacaoServ.setValue(BigDecimal.ZERO);
        if (estoqueok) {
            if (!TfCodProduto.getText().equals("") && !TfProduto.getText().equals("")) {
                if (TfValorProduto.getValue() != BigDecimal.valueOf(0) || TfQuantidade.getValue() != BigDecimal.ZERO) {
                    for (int i = 0; i < TbProdutos.getRowCount(); i++) {
                        if (Integer.parseInt(TbProdutos.getValueAt(i, 1).toString()) == Integer.parseInt(TfCodProduto.getText())) {
                            inclui = false;
                            if (JOptionPane.showConfirmDialog(null, "Este produto já foi adicionado a esta operação!\n"
                                    + "Deseja substituir a quantidade deste produto?", "Produto adicionado", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                                TbProdutos.setValueAt(TfQuantidade.getText(), i, 3);
                                TbProdutos.setValueAt(TfValorUnitario.getText(), i, 4);
                                TbProdutos.setValueAt(TfDesconto.getText(), i, 5);
                                TbProdutos.setValueAt(TfValorProduto.getText(), i, 6);
                                if(CbPromocao.isSelected()){
                                    TbProdutos.setValueAt("S", i, 7);
                                }else{
                                    TbProdutos.setValueAt("N", i, 7);
                                }
                            }
                        }
                    }
                    if (inclui) {
                        DefaultTableModel tabela = (DefaultTableModel) TbProdutos.getModel();
                        int linha = tabela.getRowCount();
                        tabela.setNumRows(linha + 1);
                        tabela.setValueAt(TfCodigo.getText(), linha, 0);
                        tabela.setValueAt(TfCodProduto.getText(), linha, 1);
                        tabela.setValueAt(TfProduto.getText(), linha, 2);
                        tabela.setValueAt(TfQuantidade.getText(), linha, 3);
                        tabela.setValueAt(TfValorUnitario.getText(), linha, 4);
                        tabela.setValueAt(TfDesconto.getText(), linha, 5);
                        tabela.setValueAt(TfValorProduto.getText(), linha, 6);
                        if(CbPromocao.isSelected()){
                            tabela.setValueAt("S", linha, 7);
                        }else{
                            tabela.setValueAt("N", linha, 7);
                        }
                    }
                    TfCodProduto.setText("");
                    TfProduto.setText("");
                    TfQuantidade.setValue(BigDecimal.valueOf(0));
                    TfValorUnitario.setValue(BigDecimal.valueOf(0));
                    TfDesconto.setValue(BigDecimal.ZERO);
                    TfValorProduto.setValue(BigDecimal.valueOf(0));
                    atualizavalortotal();
                } else {
                    msg.CampoNaoPreenchido(LbNotificacao, "Digite o valor unitário do produto e a quantidade corretamente!");
                }
            } else {
                msg.CampoNaoPreenchido(LbNotificacao, "Informe o produto a ser adicionado nesta operação!");
                TfCodProduto.grabFocus();
            }
        }
    }//GEN-LAST:event_BtIncluirProdutoTabelaActionPerformed

