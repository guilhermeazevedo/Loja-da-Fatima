    private void BtIncluirServTabelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtIncluirServTabelaActionPerformed
        boolean inclui = true;
        limpar.Limpar(TbProdutos);
        TfValorTotalOperacao.setValue(BigDecimal.ZERO);
            if (!TfCodServico.getText().equals("") && !TfServico.getText().equals("")) {
                if (TfValorServ.getValue() != BigDecimal.valueOf(0) || TfQuantidadeServ.getValue() != BigDecimal.ZERO) {
                    for (int i = 0; i < TbServicos.getRowCount(); i++) {
                        if (Integer.parseInt(TbServicos.getValueAt(i, 1).toString()) == Integer.parseInt(TfCodServico.getText())) {
                            inclui = false;
                            if (JOptionPane.showConfirmDialog(null, "Este serviço já foi adicionado a esta operação!\n"
                                    + "Deseja substituir a quantidade deste serviço?", "Serviço adicionado", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                                TbServicos.setValueAt(TfQuantidadeServ.getText(), i, 3);
                                TbServicos.setValueAt(TfValorUnitarioServ.getText(), i, 4);
                                TbServicos.setValueAt(TfDescontoServ.getText(), i, 5);
                                TbServicos.setValueAt(TfValorServ.getText(), i, 6);
                                if(CbPromocaoServ.isSelected()){
                                    TbServicos.setValueAt("S", i, 7);
                                }else{
                                    TbServicos.setValueAt("N", i, 7);
                                }
                            }
                        }
                    }
                    if (inclui) {
                        DefaultTableModel tabela = (DefaultTableModel) TbServicos.getModel();
                        int linha = tabela.getRowCount();
                        tabela.setNumRows(linha + 1);
                        tabela.setValueAt(TfCodigo.getText(), linha, 0);
                        tabela.setValueAt(TfCodServico.getText(), linha, 1);
                        tabela.setValueAt(TfServico.getText(), linha, 2);
                        tabela.setValueAt(TfQuantidadeServ.getText(), linha, 3);
                        tabela.setValueAt(TfValorUnitarioServ.getText(), linha, 4);
                        tabela.setValueAt(TfDescontoServ.getText(), linha, 5);
                        tabela.setValueAt(TfValorServ.getText(), linha, 6);
                        if(CbPromocaoServ.isSelected()){
                            tabela.setValueAt("S", linha, 7);
                        }else{
                            tabela.setValueAt("N", linha, 7);
                        }
                    }
                    TfCodServico.setText("");
                    TfServico.setText("");
                    TfQuantidadeServ.setValue(BigDecimal.valueOf(0));
                    TfValorUnitarioServ.setValue(BigDecimal.valueOf(0));
                    TfDescontoServ.setValue(BigDecimal.ZERO);
                    TfValorServ.setValue(BigDecimal.valueOf(0));
                    atualizavalortotalserv();
                } else {
                    msg.CampoNaoPreenchido(LbNotificacao, "Digite o valor unitário do serviço e a quantidade corretamente!");
                }
            } else {
                msg.CampoNaoPreenchido(LbNotificacao, "Informe o serviço a ser adicionado nesta operação!");
                TfCodProduto.grabFocus();
            }
        
    }//GEN-LAST:event_BtIncluirServTabelaActionPerformed

